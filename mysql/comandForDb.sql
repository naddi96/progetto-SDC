CREATE DATABASE vadb;
use vadb;

CREATE TABLE vatable (
  id INT AUTO_INCREMENT PRIMARY KEY,
  percentuale VARCHAR(32) NOT NULL,
  regione VARCHAR(100) NOT NULL,
  data TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
)  ENGINE=INNODB;


CREATE TABLE richiestespo (
  id INT AUTO_INCREMENT PRIMARY KEY,
  codicefiscale VARCHAR(100) NOT NULL,
  partenza VARCHAR(100) NOT NULL,
  arrivo VARCHAR(100) NOT NULL,
  esito VARCHAR(20) NOT NULL,
  data TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
)  ENGINE=INNODB;

CREATE TABLE report (
  id INT AUTO_INCREMENT PRIMARY KEY,
  url VARCHAR(100) NOT NULL,
  ip VARCHAR(50) NOT NULL,
  cookie VARCHAR(150),
  username VARCHAR(20),
  tipo VARCHAR(20),
  password VARCHAR(20),
  stato VARCHAR(100),
  esito BIT,
  data TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
)  ENGINE=INNODB;







